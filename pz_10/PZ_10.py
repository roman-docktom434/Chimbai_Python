"""
Туристические агентства предлагают следующие туры. Вояж – Мексика,Канада,Израиль,
Италия,США. РейнаТур – Англия,Япония,Канада,ЮАР. Радуга – США,Испания,Швеция,
Австралия.
Определить в каких турагентствах можно приобрести туры в Канаду, а в каких в США.
"""

# Множества стран, предлагаемых каждым агентством
voyage = {'Мексика', 'Канада', 'Израиль', 'Италия', 'США'}
reina_tour = {'Англия', 'Япония', 'Канада', 'ЮАР'}
raduga = {'США', 'Испания', 'Швеция', 'Австралия'}

# Множества для поиска
canada = {'Канада'}
usa = {'США'}

# Определение агентств, предлагающих туры в Канаду
canada_agencies = set()
if voyage & canada:
    canada_agencies.add('Вояж')
if reina_tour & canada:
    canada_agencies.add('РейнаТур')
if raduga & canada:
    canada_agencies.add('Радуга')

# Определение агентств, предлагающих туры в США
usa_agencies = set()
if voyage & usa:
    usa_agencies.add('Вояж')
if reina_tour & usa:
    usa_agencies.add('РейнаТур')
if raduga & usa:
    usa_agencies.add('Радуга')

# Вывод результатов
print("Туры в Канаду предлагаются в агентствах:", ', '.join(canada_agencies))
print("Туры в США предлагаются в агентствах:", ', '.join(usa_agencies))

