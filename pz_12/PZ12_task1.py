import random

# Ввод количества чисел
N = int(input("Введите количество чисел: "))

# Генерация исходной последовательности случайных целых чисел от 1 до 100
sequence = [random.randint(1, 100) for i in range(N)]

# Формирование последовательности чётных чисел с использованием генератора списков
evens = [x for x in sequence if x % 2 == 0]

# Формирование последовательности для остальных чисел (нечётных)
others = [x for x in sequence if x % 2 != 0]

# Вычисление среднего арифметического с использованием тернарного оператора для проверки деления на ноль
mean_evens = sum(evens) / len(evens) if evens else 0
mean_others = sum(others) / len(others) if others else 0

# Вывод результатов
print("Исходная последовательность:", sequence)
print("Последовательность чётных чисел:", evens)
print("Среднее арифметическое чётных чисел:", mean_evens)
print("Последовательность остальных чисел:", others)
print("Среднее арифметическое остальных чисел:", mean_others)
